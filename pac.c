#define WIDX 1400		//ウィンドウサイズ
#define WIDY 800
#define BPX 300			//左右空白
#define BPY -10			//上下空白
#define MAPY 117
#define MAPX 109



void stage(int map[][MAPX])
{
	int i, j;
	HgSetFillColor(HG_BLACK);
	HgBoxFill(0, 0, WIDX, WIDY, 0);
	HgSetWidth(2);
	HgSetColor(HG_BLUE);
	for(i=0; i<MAPY; i++){
		for(j=0; j<MAPX; j++){
			switch(map[i][j]){
				case 0:				//通路
					break;
				case 1:
					break;									//見えない壁
				case 2:
					HgLine(BPX+j*7, BPY+i*7+3.5, BPX+(j+1)*7, BPY+i*7+3.5); //横線
					break;
				case 3:
					HgLine(BPX+j*7+3.5, BPY+i*7, BPX+j*7+3.5, BPY+(i+1)*7); //縦線
					break;
				case 4:
					HgArc(BPX+j*7, BPY+i*7, 3.5, 0, 0.5*M_PI);		//角(右上)
					break;									
				case 5:												
					HgArc(BPX+(j+1)*7, BPY+i*7, 3.5, 0.5*M_PI, M_PI);	//角(左上)
					break;
				case 6:
					HgArc(BPX+(j+1)*7, BPY+(i+1)*7, 3.5, M_PI, 1.5*M_PI);	//角（左下)
					break;
				case 7:
					HgArc(BPX+j*7, BPY+(i+1)*7, 3.5, 1.5*M_PI, 2*M_PI);		//角(右下)
					break;
				case 8:
					HgSetFillColor(HG_YELLOW);						//小さいエサ
					HgCircleFill(BPX+j*7+3.5, BPY+i*7+3.5, 3, 0);
					break;	
				case 9:												//大きいエサ
					HgSetFillColor(HG_YELLOW);
					HgCircleFill(BPX+j*7+3.5, BPY+i*7+3.5, 7, 0);
					break;
			}
		}
	}
}

typedef struct Pacman		//パックマンの構造体
{
	int xpos, ypos;		//マップ座標
	int dx, dy;			//動作量
	double x, y;		//ウィンドウ座標
	double facedir;		//顔の向き
} Pac;

typedef struct Renemy		//赤色の敵の構造体
{
	int xpos, ypos;
	double x, y;
} Red;

typedef struct Penemy		//ピンク色の敵の構造体
{
	int xpos, ypos;
	double x, y;
} Pink;


int pacflag(int key)		//キーから移動フラグ取得
{
	switch(key){
		case HG_R_ARROW:		//右	
			return 1;
		case HG_L_ARROW:		//左
			return 2;
		case HG_U_ARROW:		//上		
			return 3;
		case HG_D_ARROW:		//下
			return 4;
		case 'q':				//終了
			exit(0);
		default:
			return 0;
	}
}

Pink pinkmove(Pink pink, Pac pac, int enemymap[][MAPX], int mvflag)	//パックマンの3歩先を最短経路で目指す, 目標が壁内部にある場合はパックマンを赤と同様に追う
{
	int i,j,m,n; 		//カウンタ変数
	int y[1200]={};		//調べる座標を入れる, オーバーフローしないよう配列を最大値より多めに設定
	int x[1200]={};
	enemymap[pink.ypos][pink.xpos]=11;
	switch(mvflag){		//パックマンの向き
		case 1:
			if(enemymap[pac.ypos][pac.xpos+9]==0||enemymap[pac.ypos][pac.xpos+9]==8||enemymap[pac.ypos][pac.xpos+9]==9||enemymap[pac.ypos][pac.xpos+9]==10){
				enemymap[pac.ypos][pac.xpos+9]=12;
				x[0]=pac.xpos+9;
				y[0]=pac.ypos;
			} else {
				enemymap[pac.ypos][pac.xpos]=12;
				x[0]=pac.xpos;
				y[0]=pac.ypos;
			}
			break;
		case 2:
			if(enemymap[pac.ypos][pac.xpos-9]==0||enemymap[pac.ypos][pac.xpos-9]==8||enemymap[pac.ypos][pac.xpos-9]==9||enemymap[pac.ypos][pac.xpos-9]==10){
				enemymap[pac.ypos][pac.xpos-9]=12;
				x[0]=pac.xpos-9;
				y[0]=pac.ypos;
			} else {
				enemymap[pac.ypos][pac.xpos]=12;
				x[0]=pac.xpos;
				y[0]=pac.ypos;
			}
			break;
		case 3:
			if(enemymap[pac.ypos+9][pac.xpos]==0||enemymap[pac.ypos+9][pac.xpos]==8||enemymap[pac.ypos+9][pac.xpos]==9||enemymap[pac.ypos+9][pac.xpos]==10){
				enemymap[pac.ypos+9][pac.xpos]=12;
				x[0]=pac.xpos;
				y[0]=pac.ypos+9;
			} else {
				enemymap[pac.ypos][pac.xpos]=12;
				x[0]=pac.xpos;
				y[0]=pac.ypos;
			}
			break;
		case 4:
			if(enemymap[pac.ypos-9][pac.xpos]==0||enemymap[pac.ypos-9][pac.xpos]==8||enemymap[pac.ypos-9][pac.xpos]==9||enemymap[pac.ypos-9][pac.xpos]==10){
				enemymap[pac.ypos-9][pac.xpos]=12;
				x[0]=pac.xpos;
				y[0]=pac.ypos-9;
			} else {
				enemymap[pac.ypos][pac.xpos]=12;
				x[0]=pac.xpos;
				y[0]=pac.ypos;
			}
			break;
		default:
			if(enemymap[pac.ypos][pac.xpos-9]==0||enemymap[pac.ypos][pac.xpos-9]==8||enemymap[pac.ypos][pac.xpos-9]==9||enemymap[pac.ypos][pac.xpos-9]==10){
				enemymap[pac.ypos][pac.xpos-9]=12;
				x[0]=pac.xpos-9;
				y[0]=pac.ypos;
			} else {
				enemymap[pac.ypos][pac.xpos]=12;
				x[0]=pac.xpos;
				y[0]=pac.ypos;
			}
			break;

	}
	m=0;
	n=1;
	for(;;){					
		i=y[m];
		j=x[m];					//パックマンの上下左右の座標を調べ, 調べた先の座標の上下左右を調べるを敵に到達するまで繰り返す

		if(enemymap[i+1][j]==11){		//敵に到達した場合は次の座標を返す
			pink.ypos=i;
			pink.xpos=j;
			pink.y=7*pink.ypos+BPY+3.5;
			pink.x=7*pink.xpos+BPX+3.5;
			return pink;
		} else if(enemymap[i+1][j]!=1){	//到達しなかった場合で壁がなければ次も調べるために配列に格納
			enemymap[i+1][j]=1;
			x[m+n]=j;
			y[m+n]=i+1;
			n++;
		}
		if(enemymap[i-1][j]==11){
			pink.ypos=i;
			pink.xpos=j;
			pink.y=7*pink.ypos+BPY+3.5;
			pink.x=7*pink.xpos+BPX+3.5;
			return pink;
		} else if(enemymap[i-1][j]!=1){
			enemymap[i-1][j]=1;
			x[m+n]=j;
			y[m+n]=i-1;
			n++;
		}
		if(enemymap[i][j+1]==11){
			pink.ypos=i;
			pink.xpos=j;
			pink.y=7*pink.ypos+BPY+3.5;
			pink.x=7*pink.xpos+BPX+3.5;
			return pink;
		} else if(enemymap[i][j+1]!=1){
			enemymap[i][j+1]=1;
			x[m+n]=j+1;
			y[m+n]=i;
			n++;
		}
		if(enemymap[i][j-1]==11){
			pink.ypos=i;
			pink.xpos=j;
			pink.y=7*pink.ypos+BPY+3.5;
			pink.x=7*pink.xpos+BPX+3.5;
			return pink;
		} else if(enemymap[i][j-1]!=1){
			enemymap[i][j-1]=1;
			x[m+n]=j-1;
			y[m+n]=i;
			n++;
		}	
		enemymap[i][j]=1;
		m++;
		n--;
	}
	return pink;
}

