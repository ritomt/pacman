Pac pacmove(int mvflag, Pac pac, int map[][MAPX])			//パックマンの移動フラグを取得してその方向に壁がなければ移動させる
{
	switch(mvflag){
		case 1:
			if(map[pac.ypos][pac.xpos+1]!=1) {
				pac.dx=1;
				pac.dy=0;
				pac.facedir=1;
			}
			break;
		case 2:
			if(map[pac.ypos][pac.xpos-1]!=1) {
				pac.dx=-1;
				pac.dy=0;
				pac.facedir=0;
			}
			break;
		case 3:
			if(map[pac.ypos+1][pac.xpos]!=1) {
				pac.dy=1;
				pac.dx=0;
				pac.facedir=-0.5;
			}
			break;
		case 4:
			if(map[pac.ypos-1][pac.xpos]!=1) {
				pac.dy=-1;
				pac.dx=0;
				pac.facedir=0.5;
			}
			break;
		default:
			break;
	}
	if(map[pac.ypos][pac.xpos+pac.dx]==1) pac.dx=0;		//パックマンの壁判定
	if(map[pac.ypos+pac.dy][pac.xpos]==1) pac.dy=0;
	pac.xpos+=pac.dx;
	pac.ypos+=pac.dy;

	if(pac.xpos==9) pac.xpos=99;				//ワープ通路通過時の座標移動
	else if(pac.xpos==99) pac.xpos=9;

	pac.x=7*pac.xpos+BPX+3.5;
	pac.y=7*pac.ypos+BPY+3.5;
	return pac;
}

int touchcheck(Pac pac, Red red, Pink pink)
{
	if(((pac.xpos==red.xpos)&&(-1<pac.ypos-red.ypos)&&(pac.ypos-red.ypos<1))||((pac.ypos==red.ypos)&&(-1<pac.xpos-red.xpos)&&(pac.xpos-red.xpos<1))
	 ||((pac.xpos==pink.xpos)&&(-1<pac.ypos-pink.ypos)&&(pac.ypos-pink.ypos<1))||((pac.ypos==pink.ypos)&&(-1<pac.xpos-pink.xpos)&&(pac.xpos-pink.xpos<1))){
		return 1;
	}
	return 0;
}

